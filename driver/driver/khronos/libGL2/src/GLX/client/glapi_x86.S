#if defined(PTHREADS)

#  define GL_STUB(fn)                       \
ALIGNTEXT16;                                \
GLOBL_FN(GL_PREFIX(fn));                    \
GL_PREFIX(fn):                              \
    MOV_L(GLNAME(_glapi_Dispatch), EAX);    \
    TEST_L(EAX, EAX);                       \
    JE(1f);                                 \
    JMP(GL_OFFSET(_gloffset_##fn));         \
1:  CALL(_x86_get_dispatch);                \
    JMP(GL_OFFSET(_gloffset_##fn))

#else /* Non-threaded version. */

#define GL_STUB(fn)                         \
ALIGNTEXT16;                                \
GLOBL_FN(GL_PREFIX(fn));                    \
GL_PREFIX(fn):                              \
    MOV_L(GLNAME(_glapi_Dispatch), EAX);    \
    JMP(GL_OFFSET(_gloffset_##fn))

#endif


/* Macro for defining API function alias.
 */
#define GL_STUB_ALIAS(fn,alias)             \
    .globl  GL_PREFIX(fn);                  \
    .set    GL_PREFIX(fn), GL_PREFIX(alias)


