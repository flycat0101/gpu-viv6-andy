{
    gctINT ch;

    while (gcvTRUE) {
        while ((ch = input()) != '*' && ch != EOF) DUMP_SOURCE_CHAR(ch);

        if (ch == EOF) {
            cloCOMPILER_Report(Compiler,
                    CurrentLineNo,
                    CurrentStringNo,
                    clvREPORT_ERROR,
                    "invalid comment: unexpected end of file");
            break;
        }
        DUMP_SOURCE_CHAR(ch);
        while ((ch = input()) == '*') DUMP_SOURCE_CHAR(ch);
        if (ch == EOF) {
            cloCOMPILER_Report(Compiler,
                    CurrentLineNo,
                    CurrentStringNo,
                    clvREPORT_ERROR,
                    "invalid comment: unexpected end of file");
            break;
        }
        else if (ch == '/') {
            DUMP_SOURCE_CHAR(ch);
            break;
        }
        else {
            DUMP_SOURCE_CHAR(ch);
        }
    }
    DUMP_SOURCE_COMMENT_END();
}

